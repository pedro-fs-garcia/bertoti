
public class Aluno {
    private String nome;
    private int idade;

    public String getNome(){
        return this.nome;
    }

    public void setNome (String newNome){
        this.nome = newNome;
    }

    public int getIdade(){
        return this.idade;
    }

    public void setIdade(int newIdade){
        this.idade = newIdade;
    }
}
